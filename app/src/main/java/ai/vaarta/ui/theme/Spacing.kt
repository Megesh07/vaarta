package ai.vaarta.ui.theme

import androidx.compose.ui.unit.dp

/**
 * The one spacing scale (Calm Guardian design system §6). Screens use these tokens, never ad-hoc dp
 * values, so vertical rhythm is identical everywhere.
 */
object VSpace {
    val xs = 4.dp
    val sm = 8.dp
    val md = 12.dp
    val lg = 16.dp
    val xl = 20.dp
    val xxl = 24.dp
    val xxxl = 32.dp
}
